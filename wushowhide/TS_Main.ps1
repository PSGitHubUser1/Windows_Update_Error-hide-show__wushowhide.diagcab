function ispostbackOnWin8($packName)
{
	[string] $path1 = (Get-Location -PSProvider FileSystem).ProviderPath
	[string] $path1 = join-path  $path1  "\$packName"
	if(test-path $path1){
		# del $path1 -force
		# detecting additional problem
		return $true
	}
	"once" > $path1 
	return $false
}

